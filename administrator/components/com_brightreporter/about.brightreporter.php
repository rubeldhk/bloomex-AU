<P align="left">Brightcode Reporter is a very simple but powerfull Joomla! Report Tool. </P>
<P align="left">You can put in any query you want and get the results on screen or in a Excel file. </P>
<P align="left">You can save reports (give them a title) You can publish and unpublish reports </P>
<P align="left">You can call the reports from the frontend and backend of your Joomla! site. </P>
<P align="left">You can create your own simple or difficult queries. You can use any table (multiple tables) from your joomla! dbase. </P>
<P align="left">You can create Query variables like ${someinfo}  so you can create dynamic reports. </P>
<P align="left">You can Set Date / String or even select params for your report. </P>
<P align="left">Select Box param can be filed by a query string or a self created list (eg: Buy|Sell|Hire|For Rent|For Free ) </P>

